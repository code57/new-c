#include <stdio.h>
int main()//对于浮点类型存取的理解
{
	int a = 9;//09 00 00 00 || 00001010 00000000 000000000 00000000
	float* pf = (float*)&a;//0 00010100 00000000000000000000000
	printf("%f\n", *pf);
	*pf = 9.0;
	printf("%f\n", *pf);//00000000 00000000 00010000 01000001 存储中顺序 ，即a b c d
	                    //0 10000010 00100000001000000000000 理解顺序,即 d c b a
	printf("%d\n", a);
	return 0;
}
